// 邮箱
export let verifyEmail = /^([a-zA-Z]|[0-9])(\w|\-)+@[a-zA-Z0-9]+\.([a-zA-Z]{2,4})$/;

// 验证密码 长度为8且至少包含数字、字母、特殊字符两种
export let verifyPwd = /^(?!\d+$)(?!^[a-zA-Z]+$)(?!^[_#@]+$).{8,}$/;
