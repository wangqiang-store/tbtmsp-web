// 正数、一位小数
export let verifyFigure = /^(0|\d{1,}|(0|[1-9]*)+(\.[0-9]{1,1})|([1-9]\d*))?$/;

// 正数
export let verifyPlus = /^(0|\d{1,}|(0|[1-9]*)+(\.[0-9]{1,})|([1-9]\d*))?$/;

// 正负数 
export let verifyPlusMinus = /^-?(0|\d{1,}|(0|[1-9]*)+(\.[0-9]{1,1})|([1-9]\d*))?$/;

// 正整数
export let verifyInt = /^(0|\d{1,}|)$/;

// 整数
export let verifyInteger = /^(0|-?\d{1,}|)$/;

// 数字在0~100小数保留两位小数
export let verify_0_100 = /^(\d{1,2}(\.\d{1,2})?|100|100\.0|100\.00)$/;

// 电话
export let verifyPhone = /^1(3\d|4[5-9]|5[0-35-9]|6[2567]|7[0-8]|8\d|9[0-35-9])\d{8}$/;

